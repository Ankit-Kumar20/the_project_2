import type { NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { useRouter } from "next/router";
import { useSession, signOut } from "@/lib/auth-client";
import { useState } from "react";
import styles from "../styles/Home.module.css";

const Home: NextPage = () => {
  const { data: session, isPending } = useSession();
  const router = useRouter();
  const [from, setFrom] = useState("");
  const [to, setTo] = useState("");
  const [days, setDays] = useState("");
  const [stops, setStops] = useState("");
  const [loading, setLoading] = useState(false);
  const [showModal, setShowModal] = useState(false);

  const handleSignOut = async () => {
    await signOut();
  };

  const handleOpenCanvas = async () => {
    setLoading(true);
    try {
      const response = await fetch("/api/generate-travel-flow", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ from, to, days: Number(days), stops }),
      });

      const result = await response.json();
      
      if (result.success) {
        setShowModal(false);
        router.push({
          pathname: "/canvas",
          query: { flowData: JSON.stringify(result.data.flow) },
        });
      } else {
        alert("Failed to generate travel flow");
      }
    } catch (error) {
      console.error("Error:", error);
      alert("An error occurred");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>Welcome to Next.js on Replit!</h1>

        {isPending ? (
          <p className={styles.description}>Loading...</p>
        ) : session?.user ? (
          <div>
            <p className={styles.description}>
              Welcome, {String(session.user.name || session.user.email || "User")}!
            </p>
            <div style={{ marginTop: "20px", display: "flex", gap: "10px", justifyContent: "center" }}>
              <button
                onClick={() => setShowModal(true)}
                style={{
                  padding: "12px 30px",
                  fontSize: "16px",
                  backgroundColor: "#0070f3",
                  color: "white",
                  border: "none",
                  borderRadius: "5px",
                  cursor: "pointer",
                  fontWeight: "600",
                }}
              >
                New Trip
              </button>
              <button
                onClick={handleSignOut}
                style={{
                  padding: "10px 20px",
                  fontSize: "16px",
                  backgroundColor: "#ff0000",
                  color: "white",
                  border: "none",
                  borderRadius: "5px",
                  cursor: "pointer",
                }}
              >
                Sign Out
              </button>
            </div>

            {showModal && (
              <div
                style={{
                  position: "fixed",
                  top: 0,
                  left: 0,
                  right: 0,
                  bottom: 0,
                  backgroundColor: "rgba(0, 0, 0, 0.5)",
                  display: "flex",
                  justifyContent: "center",
                  alignItems: "center",
                  zIndex: 1000,
                }}
                onClick={() => setShowModal(false)}
              >
                <div
                  style={{
                    backgroundColor: "white",
                    padding: "30px",
                    borderRadius: "10px",
                    maxWidth: "500px",
                    width: "90%",
                    boxShadow: "0 4px 20px rgba(0, 0, 0, 0.2)",
                  }}
                  onClick={(e) => e.stopPropagation()}
                >
                  <h2 style={{ marginTop: 0, marginBottom: "20px", color: "#333" }}>
                    Plan Your Trip
                  </h2>
                  <div style={{ marginBottom: "15px" }}>
                    <input
                      type="text"
                      placeholder="From (e.g., Delhi)"
                      value={from}
                      onChange={(e) => setFrom(e.target.value)}
                      style={{
                        width: "100%",
                        padding: "10px",
                        fontSize: "14px",
                        borderRadius: "5px",
                        border: "1px solid #ccc",
                      }}
                    />
                  </div>
                  <div style={{ marginBottom: "15px" }}>
                    <input
                      type="text"
                      placeholder="To (e.g., Goa)"
                      value={to}
                      onChange={(e) => setTo(e.target.value)}
                      style={{
                        width: "100%",
                        padding: "10px",
                        fontSize: "14px",
                        borderRadius: "5px",
                        border: "1px solid #ccc",
                      }}
                    />
                  </div>
                  <div style={{ marginBottom: "15px" }}>
                    <input
                      type="number"
                      placeholder="Days (e.g., 7)"
                      value={days}
                      onChange={(e) => setDays(e.target.value)}
                      style={{
                        width: "100%",
                        padding: "10px",
                        fontSize: "14px",
                        borderRadius: "5px",
                        border: "1px solid #ccc",
                      }}
                    />
                  </div>
                  <div style={{ marginBottom: "20px" }}>
                    <input
                      type="text"
                      placeholder="Stops (optional, e.g., Mumbai, Pune)"
                      value={stops}
                      onChange={(e) => setStops(e.target.value)}
                      style={{
                        width: "100%",
                        padding: "10px",
                        fontSize: "14px",
                        borderRadius: "5px",
                        border: "1px solid #ccc",
                      }}
                    />
                  </div>
                  <div style={{ display: "flex", gap: "10px", justifyContent: "flex-end" }}>
                    <button
                      onClick={() => setShowModal(false)}
                      style={{
                        padding: "10px 20px",
                        fontSize: "16px",
                        backgroundColor: "#ccc",
                        color: "#333",
                        border: "none",
                        borderRadius: "5px",
                        cursor: "pointer",
                      }}
                    >
                      Cancel
                    </button>
                    <button
                      onClick={handleOpenCanvas}
                      disabled={loading}
                      style={{
                        padding: "10px 20px",
                        fontSize: "16px",
                        backgroundColor: loading ? "#ccc" : "#0070f3",
                        color: "white",
                        border: "none",
                        borderRadius: "5px",
                        cursor: loading ? "not-allowed" : "pointer",
                      }}
                    >
                      {loading ? "Generating..." : "Generate Trip"}
                    </button>
                  </div>
                </div>
              </div>
            )}
          </div>
        ) : (
          <div>
            <p className={styles.description}>
              Please <Link href="/login">sign in</Link> or{" "}
              <Link href="/signup">sign up</Link> to continue.
            </p>
          </div>
        )}

        <p className={styles.description}>
          Get started by editing{" "}
          <code className={styles.code}>pages/index.tsx</code>
        </p>

        <div className={styles.grid}>
          <a href="https://nextjs.org/docs" className={styles.card}>
            <h2>Documentation &rarr;</h2>
            <p>Find in-depth information about Next.js features and API.</p>
          </a>

          <a href="https://nextjs.org/learn" className={styles.card}>
            <h2>Learn &rarr;</h2>
            <p>Learn about Next.js in an interactive course with quizzes!</p>
          </a>

          <a
            href="https://github.com/vercel/next.js/tree/canary/examples"
            className={styles.card}
          >
            <h2>Examples &rarr;</h2>
            <p>Discover and deploy boilerplate example Next.js projects.</p>
          </a>

          <a
            href="https://docs.replit.com/category/deployments"
            className={styles.card}
          >
            <h2>Deploy &rarr;</h2>
            <p>After you are happy with this app, deploy it on Replit!</p>
          </a>
        </div>
      </main>
    </div>
  );
};

export default Home;
